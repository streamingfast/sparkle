func secureIntn(max int) (int, error) {
	maxBigInt := big.NewInt(int64(max))
	randomBigInt, err := secureRand.Int(secureRand.Reader, maxBigInt)
	return int(randomBigInt.Int64()), err
}

func GetRandomNames(number int) ([]string, error) {
	var names []string
	for i := 0; i < number; i++ {
		randLeft, err := secureIntn(len(left))
		if err != nil {
			return nil, err
		}
		randRight, err := secureIntn(len(right))
		if err != nil {
			return nil, err
		}
		names = append(names, fmt.Sprintf("%s-%s", left[randLeft], right[randRight]))
	}
	return names, nil
}
